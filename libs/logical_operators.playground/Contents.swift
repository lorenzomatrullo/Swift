// Logical Operators
/*
    ==      two values must be equal
    !=      two values must not be equal
    >       A value is greater than another value
    >=      A value is greater or equal to another value
    <       A value is less than another value
    <=      A value is less or equal to another value
    &&      AND - left and right conditions must be true
    ||      OR -  one between left or right condition must be true
    !       Returns the opposite of a condition
*/
